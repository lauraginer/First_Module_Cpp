#include "HumanB.hpp"
#include <iostream>

HumanB::HumanB(std::string name) : name(name), weapon(NULL) //referenciamos a NULL, ya que no es obligatorio tener arma
{
}

void HumanB::setWeapon(Weapon& arm)
{
	weapon = &arm;
}

void HumanB::attack()
{
	if (weapon)
		std::cout << name << " attacks with their " << weapon->getType() << std::endl;
	else
		std::cout << name << " has no weapon" << std::endl;
}
